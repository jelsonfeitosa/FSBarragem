package com.leenadam.app.Usina;

public class DadosUsina {

    private String nomeUsina;
    private int idUsina;
    private double potenciaUsina;

    private String nomeRioUsina;
    private String municipioUsina;
    private String estadoUsina;

    private double capacidadeReservatorioUsina;
    private String dataPrimeiroEnchimentoUsina;
    private int numetoTotalBarramentosUsina;

    public String getNomeUsina() {
        return nomeUsina;
    }

    public void setNomeUsina(String nomeUsina) {
        this.nomeUsina = nomeUsina;
    }

    public int getIdUsina() {
        return idUsina;
    }

    public void setIdUsina(int idUsina) {
        this.idUsina = idUsina;
    }

    public double getPotenciaUsina() {
        return potenciaUsina;
    }

    public void setPotenciaUsina(double potenciaUsina) {
        this.potenciaUsina = potenciaUsina;
    }

    public String getNomeRioUsina() {
        return nomeRioUsina;
    }

    public void setNomeRioUsina(String nomeRioUsina) {
        this.nomeRioUsina = nomeRioUsina;
    }

    public String getMunicipioUsina() {
        return municipioUsina;
    }

    public void setMunicipioUsina(String municipioUsina) {
        this.municipioUsina = municipioUsina;
    }

    public String getEstadoUsina() {
        return estadoUsina;
    }

    public void setEstadoUsina(String estadoUsina) {
        this.estadoUsina = estadoUsina;
    }

    public double getCapacidadeReservatorioUsina() {
        return capacidadeReservatorioUsina;
    }

    public void setCapacidadeReservatorioUsina(double capacidadeReservatorioUsina) {
        this.capacidadeReservatorioUsina = capacidadeReservatorioUsina;
    }

    public String getDataPrimeiroEnchimentoUsina() {
        return dataPrimeiroEnchimentoUsina;
    }

    public void setDataPrimeiroEnchimentoUsina(String dataPrimeiroEnchimentoUsina) {
        this.dataPrimeiroEnchimentoUsina = dataPrimeiroEnchimentoUsina;
    }

    public int getNumetoTotalBarramentosUsina() {
        return numetoTotalBarramentosUsina;
    }

    public void setNumetoTotalBarramentosUsina(int numetoTotalBarramentosUsina) {
        this.numetoTotalBarramentosUsina = numetoTotalBarramentosUsina;
    }
}
